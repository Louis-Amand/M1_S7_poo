/**
 * The interface Product.
 */
public interface Product {
    /**
     * Gets price.
     *
     * @return the price
     */
    double getPrice();

    /**
     * Gets vat price.
     *
     * @return the vat price
     */
    double getVatPrice();

    /**
     * Gets vat amount.
     *
     * @return the vat amount
     */
    double getVatAmount();

    /**
     * Gets unit price.
     *
     * @return the unit price
     */
    double getUnitPrice();

    /**
     * Gets unit vat price.
     *
     * @return the unit vat price
     */
    double getUnitVatPrice();

    /**
     * Gets unit vat amount.
     *
     * @return the unit vat amount
     */
    double getUnitVatAmount();


}
